def soma(a, b):
    return a + b

def subtrai(a, b):
    return a - b

def main():
    x = 10
    y = 5

    resultado_soma = soma(x, y)
    resultado_subtracao = subtrai(x, y)

    print(f"Soma: {resultado_soma}")
    print(f"Subtração: {resultado_subtracao}")

if __name__ == "__main__":
    main()
